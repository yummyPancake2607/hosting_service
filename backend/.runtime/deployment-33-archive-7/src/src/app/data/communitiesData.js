// Enhanced personal information patterns
// Phone number: any 10+ digits (with or without spaces, dashes, parentheses)
const phonePattern = /\b\d[\d\s\-().]{8,}\d\b/g; // Catches 10 digits even with spaces
const strictPhonePattern = /(?:\d[\s\-().]?){9,}/g; // Even stricter - any 9+ digits with optional separators
const emailPattern = /\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b/g;
const socialMediaPattern =
  /\b(insta|instagram|snap|snapchat|twitter|facebook|fb|whatsapp|telegram|discord|tiktok|youtube|yt)[\s:]*@?[\w.-]+\b/gi;
const urlPattern = /(https?:\/\/|www\.)[^\s]+/gi;

