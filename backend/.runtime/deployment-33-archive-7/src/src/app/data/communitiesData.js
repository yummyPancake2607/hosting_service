export function filterProfanity(text, memberSince) {
  let filtered = text;
  let hasProfanity = false;
  let warningType;
  let warningMessage;

  // Check for child-unfriendly content
  const childUnfriendlyWords = [
    "sex",
    "nude",
    "naked",
    "porn",
    "xxx",
    "nsfw",
    "sexual",
    "intercourse",
  ];
  for (const word of childUnfriendlyWords) {
    const regex = new RegExp(word, "gi");
    if (regex.test(filtered)) {
      hasProfanity = true;
      warningType = "child-unfriendly";
      warningMessage =
        "⚠️ This chat is open to all ages and is meant for game discussions only. Use of inappropriate words is prohibited here.";
      filtered = filtered.replace(regex, "*".repeat(word.length));
    }
  }

  // Deep profanity detection - check for words even when combined
  profanityList.forEach((word) => {
    // Word boundary check
    const boundaryRegex = new RegExp(`\\b${word}\\b`, "gi");
    if (boundaryRegex.test(filtered)) {
      hasProfanity = true;
      if (!warningType) warningType = "profanity";
      filtered = filtered.replace(boundaryRegex, "*".repeat(word.length));
    }

    // Deep check - detect profanity even when attached to other words
    const deepRegex = new RegExp(word, "gi");
    if (deepRegex.test(filtered)) {
      hasProfanity = true;
      if (!warningType) warningType = "profanity";
      // Replace the profane part while keeping surrounding text
      filtered = filtered.replace(deepRegex, (match) =>
        "*".repeat(match.length),
      );
    }
  });

  // Filter personal information
  if (phonePattern.test(filtered)) {
    filtered = filtered.replace(phonePattern, "[PHONE NUMBER REMOVED]");
    hasProfanity = true;
    warningType = "personal-info";
    warningMessage =
      "⚠️ Do not share personal details like phone numbers. Your privacy is important!";
  }

  if (emailPattern.test(filtered)) {
    filtered = filtered.replace(emailPattern, "[EMAIL REMOVED]");
    hasProfanity = true;
    warningType = "personal-info";
    warningMessage =
      "⚠️ Do not share personal details like email addresses. Your privacy is important!";
  }

  if (socialMediaPattern.test(filtered)) {
    filtered = filtered.replace(
      socialMediaPattern,
      "[SOCIAL MEDIA ID REMOVED]",
    );
    hasProfanity = true;
    warningType = "personal-info";
    warningMessage =
      "⚠️ Do not share social media handles. Keep your personal information private!";
  }

  // Check for links and time restriction
  if (urlPattern.test(filtered)) {
    const now = new Date();
    const hoursSinceMemberJoin = memberSince
      ? (now.getTime() - memberSince.getTime()) / (1000 * 60 * 60)
      : 0;

    if (hoursSinceMemberJoin < 48) {
      filtered = filtered.replace(
        urlPattern,
        "[LINK REMOVED - Available after 48hrs]",
      );
      hasProfanity = true;
      warningType = "link-restriction";
      warningMessage =
        "⚠️ Link sharing is only allowed after 48 hours of joining the community. This helps maintain a safe environment.";
    }
  }

  // Set default warning message if not already set
  if (hasProfanity && !warningMessage) {
    warningMessage =
      "⚠️ Warning: Inappropriate language detected. Your message has been filtered. Repeated violations may result in a ban.";
  }

  return { filtered, hasProfanity, warningType, warningMessage };
}

export function getCommunitiesByGame(gameId) {
  return Object.values(communitiesDatabase)
    .filter((community) => community.gameId === gameId)
    .sort((a, b) => b.memberCount - a.memberCount);
}

export function getAllCommunities() {
  return Object.values(communitiesDatabase).sort(
    (a, b) => b.memberCount - a.memberCount,
  );
}
